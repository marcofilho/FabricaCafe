public class Ingredientes {

    private String cafeSoluvel;
    private String cafeDescafeinado;
    private String acucar;
    private String leite;
    private String bicarbonato;
    private String chocolate;
    private String canela;
    private String agua;


    public String getCafeSoluvel() {
        return cafeSoluvel;
    }

    public void setCafeSoluvel(String cafeSoluvel) {
        this.cafeSoluvel = cafeSoluvel;
    }

    public String getCafeDescafeinado() {
        return cafeDescafeinado;
    }

    public void setCafeDescafeinado(String cafeDescafeinado) {
        this.cafeDescafeinado = cafeDescafeinado;
    }

    public String getAcucar() {
        return acucar;
    }

    public void setAcucar(String acucar) {
        this.acucar = acucar;
    }

    public String getLeite() {
        return leite;
    }

    public void setLeite(String leite) {
        this.leite = leite;
    }

    public String getBicarbonato() {
        return bicarbonato;
    }

    public void setBicarbonato(String bicarbonato) {
        this.bicarbonato = bicarbonato;
    }

    public String getChocolate() {
        return chocolate;
    }

    public void setChocolate(String chocolate) {
        this.chocolate = chocolate;
    }

    public String getCanela() {
        return canela;
    }

    public void setCanela(String canela) {
        this.canela = canela;
    }

    public String getAgua() {
        return agua;
    }

    public void setAgua(String agua) {
        this.agua = agua;
    }
}
